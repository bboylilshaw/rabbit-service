package com.example;

/**
 * @author Jason Xiao
 */
public class RequestParam {

    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return "RequestParam{" +
                "id=" + id +
                '}';
    }
}
